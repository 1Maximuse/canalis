package canalis.objects;

public abstract class GameObject {
	
	protected int posX, posY;
	protected int width, height;
	
	public int getPosX() {
		return posX;
	}
	
	public int getPosY() {
		return posY;
	}
	
	public void setPosX(int x) {
		this.posX = x;
	}
	
	public void setPosY(int y) {
		this.posY = y;
	}
}
